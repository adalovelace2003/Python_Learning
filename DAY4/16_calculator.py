print("\n\n\n\n\n\n")

num_1 = int(input('Enter your first number: '))
num_2 = int(input('Enter your second number: '))

# Addition
print('\n\n {} + {} = '.format(num_1, num_2))
print(num_1 + num_2)

print("\n")
# Subtraction
print('{} - {} = '.format(num_1, num_2))
print(num_1 - num_2)

print("\n")
# Multiplication
print('{} * {} = '.format(num_1, num_2))
print(num_1 * num_2)

print("\n")
# Division
print('{} / {} = '.format(num_1, num_2))
print(num_1 / num_2)

print("\n\n\n\n\n\n")




